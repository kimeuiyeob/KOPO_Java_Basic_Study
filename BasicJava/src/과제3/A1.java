package 과제3;

public class A1 {

	public static void main(String[] args) {

//	Use an “if” statement
//	Use a double “for” loop
//	Print 2 to 9 times table

		for (int i = 2; i < 10; i++) {
			for (int j = 1; j < 10; j++) {
				System.out.println(i + " X " + j + " = " + i * j);
			}
			System.out.println();

		}
	}
}
